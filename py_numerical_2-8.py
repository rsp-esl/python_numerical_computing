import math

# show the mantissa and exponent of floating-point numbers:
# 0.1, 0.2,...1.0 (increase by 0.1 for each step)

x = 0.1; sum = 0
for i in range(10):
    sum += x
    m, e = math.frexp(sum)
    print ( '%.18f = %.18f * 2**(%d)' % (m * 2**e, m, e) )

