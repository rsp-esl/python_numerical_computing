import math

# show the mantissa and exponent of a floating-point number 

x = 1.0; dx = 1.0/(2**52)
m, e = math.frexp(x)
print ( '%.32f \n = %.18f * 2**(%d)' % (m * 2**e, m, e) )

m, e = math.frexp(dx)
print ( '%.32f \n = %.32f * 2**(%d)' % (m * 2**e, m, e) )

m, e = math.frexp(1+dx)
print ( '%.32f \n = %.32f * 2**(%d)' % (m * 2**e, m, e) )

